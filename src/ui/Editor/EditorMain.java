    private void PapulateArticleTable() {

        DefaultTableModel model = (DefaultTableModel) tblArticles.getModel();
        model.setRowCount(0);

        for (var item : articles.GetArticleList()) {

            Object[] row = new Object[8];
            row[0] = item.getId();
            row[1] = item.getRegisterTime();
            row[2] = item.getTitle();
            row[3] = item.getAuthor().toString();
            row[4] = item.getStatus();
            row[5] = item.getComment();
            row[6] = item.getLastEditTime();
            row[7] = (item.getApproval() != null ? item.getApproval().getFirstName() + " " + item.getApproval().getLastName() : "Unknown");

            model.addRow(row);
        }
    }

