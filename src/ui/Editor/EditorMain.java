    private void txtSearchArticleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchArticleKeyReleased
        // TODO add your handling code here:
        TableRowSorter<DefaultTableModel> rowSorter;
        rowSorter = new TableRowSorter<>((DefaultTableModel) tblArticles.getModel());
        tblArticles.setRowSorter(rowSorter);
        rowSorter.setRowFilter(RowFilter.regexFilter(txtSearchArticle.getText()));
    }//GEN-LAST:event_txtSearchArticleKeyReleased

    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblArticles.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to approve.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblArticles.getModel();
        int id = (int) model.getValueAt(selectedRowIndex, 0);

        var commnet = JOptionPane.showInputDialog(this, "Enter your comment for Approval:");

        articles.Approve(id, editor.getId(), commnet);
        JOptionPane.showMessageDialog(this, "Article approved successfully...");
        PapulateArticleTable();
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblArticles.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to reject.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblArticles.getModel();
        int id = (int) model.getValueAt(selectedRowIndex, 0);

        var commnet = JOptionPane.showInputDialog(this, "Enter your comment for Rejection:");

        articles.Reject(id, editor.getId(), commnet);
        JOptionPane.showMessageDialog(this, "Article rejected successfully...");
        PapulateArticleTable();
    }//GEN-LAST:event_btnRejectActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblArticles.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to View.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblArticles.getModel();
        int id = (int) model.getValueAt(selectedRowIndex, 0);
        JOptionPane.showMessageDialog(this, new dbArticle().Find(id).getMainText());
    }//GEN-LAST:event_btnViewActionPerformed


