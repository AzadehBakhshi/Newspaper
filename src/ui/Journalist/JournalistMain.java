    private void btnDeleteArticleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteArticleActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblArticles.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblArticles.getModel();
        int id = (int) model.getValueAt(selectedRowIndex, 0);
        articles.Delete(id);

        JOptionPane.showMessageDialog(this, "Selected row deleted.");

        PapulateArticleTable();
    }//GEN-LAST:event_btnDeleteArticleActionPerformed

