    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        // TODO add your handling code here:
        if (txtTitle.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please enter Title field.");
            return;
        }
        
        if (txtMain.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please enter Article field.");
            return;
        }
        
        if (txtSummary.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please enter Summary field.");
            return;
        }
        
        Article newArticle = new Article();
        
        newArticle.setTitle(txtTitle.getText());
        newArticle.setMainText(txtMain.getText());
        newArticle.setSummary(txtSummary.getText());
        newArticle.setKeywords(txtKeywords.getText());
        newArticle.setComment("");
        newArticle.setAuthor(author);
        newArticle.setApproval(null);
        newArticle.setRegisterTime(new Date());
        newArticle.setLastEditTime(new Date());
        
        article.Add(newArticle);
        
        main.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

